# 默认值：LLM 偶发输出越界类型 / 谓词时的兜底
DEFAULT_ENTITY_TYPE = "关联于"  # 占位，校验时若类型越界则归为该项的处理见 normalize
UNKNOWN_ENTITY_TYPE = "其他"
UNKNOWN_PREDICATE = "关联于"


def normalize_entity_type(t: str | None) -> str:
    """把 LLM 输出的实体类型规范到受控词表；越界则归为「其他」。"""
    label = (t or "").strip()
    return label if label in ENTITY_TYPES else UNKNOWN_ENTITY_TYPE


def normalize_predicate(p: str | None) -> str:
    """把 LLM 输出的谓词规范到受控词表；越界则归为「关联于」弱关系。"""
    label = (p or "").strip()
    return label if label in PREDICATES else UNKNOWN_PREDICATE


def is_valid_entity_type(t: str | None) -> bool:
    return (t or "").strip() in ENTITY_TYPES


def is_valid_predicate(p: str | None) -> bool:
    return (p or "").strip() in PREDICATES


